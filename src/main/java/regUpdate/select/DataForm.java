package regUpdate.select;

public class DataForm {

    private String pcName;
    private String ipAddress;
    private String executionTime;
    private String propriety;
	/**
	 * @return pcName
	 */
	public String getPcName() {
		return pcName;
	}
	/**
	 * @param pcName セットする pcName
	 */
	public void setPcName(String pcName) {
		this.pcName = pcName;
	}
	/**
	 * @return ipAddress
	 */
	public String getIpAddress() {
		return ipAddress;
	}
	/**
	 * @param ipAddress セットする ipAddress
	 */
	public void setIpAddress(String ipAddress) {
		this.ipAddress = ipAddress;
	}
	/**
	 * @return executionTime
	 */
	public String getExecutionTime() {
		return executionTime;
	}
	/**
	 * @param executionTime セットする executionTime
	 */
	public void setExecutionTime(String executionTime) {
		this.executionTime = executionTime;
	}
	/**
	 * @return propriety
	 */
	public String getPropriety() {
		return propriety;
	}
	/**
	 * @param propriety セットする propriety
	 */
	public void setPropriety(String propriety) {
		this.propriety = propriety;
	}



}
